let n = 1;

switch(n){
    case 1:
        console.log("january , february , march");
        break;
    case 2:
        console.log("april , may , june");
        break;
    case 3:
        console.log("july , august , september");
        break;
    default:
        console.log("october , november , december");
        break;
}